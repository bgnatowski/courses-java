// Przykład użycia klasy HashSet 
import java.util.*; 
 
class HashSetDemo { 
  public static void main(String args[]) { 
    // Utworzenie tablicy mieszającej 
    HashSet<String> hs = new HashSet<String>(); 
     
    // Dodanie elementów do tablicy mieszającej 
    hs.add("Beta"); 
    hs.add("Alfa"); 
    hs.add("Eta"); 
    hs.add("Gamma"); 
    hs.add("Epsilon"); 
    hs.add("Omega"); 
 
    System.out.println(hs); 
  } 
}
